public class Sach {
    private String ten, theloai;

    public Sach(String ten, String theloai) {
        this.ten = ten;
        this.theloai = theloai;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public String getTheloai() {
        return theloai;
    }

    public void setTheloai(String theloai) {
        this.theloai = theloai;
    }
}
